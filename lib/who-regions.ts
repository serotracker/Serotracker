interface GetCountryCodesInWHORegionInput {
  whoRegion: WhoRegion;
}

export const getCountryCodesInWHORegion = (
  input: GetCountryCodesInWHORegionInput
): string[] => {
  return Object.entries(countryAlphaTwoCodeToWhoRegionsMap)
    .map(([key, value]) => ({ countryCode: key, whoRegion: value }))
    .filter(({ whoRegion }) => whoRegion === input.whoRegion)
    .map(({ countryCode }) => countryCode);
};

export const isWHORegion = (whoRegion: string): whoRegion is WhoRegion =>
  Object.values(WhoRegion).some((element) => element === whoRegion);
